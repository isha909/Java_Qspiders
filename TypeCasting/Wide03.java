public class Wide03 {
  public static void main(String[] args) {

    //short to int 
    short a = 734;
    int b = a;
    System.out.println(a);
    System.out.println(b);

    //short to long
    short c = 374;
    long d = c;
    System.out.println(c);
    System.out.println(d);

    //short to float
    short e = 674;
    float f = e;
    System.out.println(e);
    System.out.println(f);

    //short to double
    short g = 345;
    double h = g;
    System.out.println(g);
    System.out.println(h);

  }
}
