public class Exp01{
  public static void main(String[] args) {
    
    //double to float 
    double a = 2345.12345678;
    float b = (float) a;
    System.out.println(a);
    System.out.println(b);


    //double to long
    double c = 245.23456789;
    long d = (long) c;
    System.out.println(c);
    System.out.println(d);


    //double to int
    double e = 876.98765432345;
    int f = (int) e;
    System.out.println(e);
    System.out.println(f);


    //double to short
    double g = 876.34567899876;
    short h = (short) g;
    System.out.println(g);
    System.out.println(h);


    //double to byte
    double i = 676.7643457899;
    byte j = (byte) i;
    System.out.println(i);
    System.out.println(j);
  }
}