public class Wide02 {
  public static void main(String[] args) {
    
    // short to int
    short a = 58;
    int b = a;
    System.out.println(a);
    System.out.println(b);

    //short to long
    long c = a;
    System.out.println(c);

    //short to float 
    float d = a;
    System.out.println(d);

    //short to double
    double e = a;
    System.out.println(e);
  }
}
