public class Exp03 {
  public static void main(String[] args) {
    
    //long to int
    long a = 87645678956l;
    int b = (int) a;
    System.out.println(a);
    System.out.println(b);

    //long to short
    long c = 234567890987l;
    short d = (short) c;
    System.out.println(c);
    System.out.println(d);
    
    //long to byte
    long e = 9876543267l;
    byte f = (byte) e;
    System.out.println(e);
    System.out.println(f);
    
  }
}
