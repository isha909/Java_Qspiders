public class Exp02 {
  public static void main(String[] args) {
    
    //float to long
    float a = 4567.345f;
    long b = (long) a;
    System.out.println(a);
    System.out.println(b);

    //float to int 
    float c = 45.9876f;
    int d = (int) c;
    System.out.println(c);
    System.out.println(d);

    //float to short
    float e = 56.987f;
    short f = (short) e;
    System.out.println(e);
    System.out.println(f);

    //float to byte
    float g = 234.8765f;
    byte h = (byte) g;
    System.out.println(g);
    System.out.println(h);

  }
}
