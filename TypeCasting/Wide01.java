public class Wide01 {
  public static void main(String[] args) {
    
    //byte to short 
    byte a = 7;
    short b = a;
    System.out.println(a);
    System.out.println(b);

    //byte to int
    byte c = 37;
    int d = a;
    System.out.println(c);
    System.out.println(d);

    //byte to long
    byte e = 67;
    long f = e;
    System.out.println(e);
    System.out.println(f);

    //byte to float
    float g = e;
    System.out.println(g);

    //byte to double
    double h = e;
    System.out.println(h);

  }
}