public class Wide4 {
  public static void main(String[] args) {
    
    //int to long
    int a = 4567;
    long b = a;
    System.out.println(a);
    System.out.println(b);

    //int to float
    float c = a;
    System.out.println(c);

    //int to double
    double d = a;
    System.out.println(d);

  }
}
