    System.out.println("Factorial of " + num4 + " is " + factorial2);

    //sum of factors of 9
    int num5 = 9;
    int fact1 = 1 , fact2 = 3 , fact3 = 9;
    int add = fact1 + fact2 + fact3;
    System.out.println("sum of factors of " + num5 + " is " + add);
    
    //product of divisor of 12
    int num6 = 12;
    int factt1 = 1, factt2 = 2, factt3 = 3, factt4 = 4, factt5 = 6, factt6 = 12;
