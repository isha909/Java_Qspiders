public class IfElseIf4 {
  public static void main(String[] args) {
    int i = 456 , j = 234;
    if(i > j){
      System.out.println(i + " is greater number");
    }
    else if(j > i){
      System.out.println(j + " is greater number");
    }
    else{
      System.out.println("Both the numbers are equal");
    }
  }
}
