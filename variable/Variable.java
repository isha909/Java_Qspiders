class Variable
{
	public static void main(String [] args)
	{
		//byte type 5 var
		//syntax : datatype var;
		System.out.println("Start");
		byte roll_no;
		byte room_no;
		byte sl_no;
		byte age;
		byte marks;
		System.out.println("Byte variable are created");

		//initialization
		roll_no=54;
		room_no=4;
		sl_no=50;
		age=21;
		marks=99;

		//access
		System.out.println(roll_no);
		System.out.println(room_no);
		System.out.println(sl_no);
		System.out.println(age);
		System.out.println(marks);
	}
}
