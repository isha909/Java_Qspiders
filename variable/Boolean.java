public class Boolean {
  public static void main(String[] args) 
  {
    System.out.println("Start");
    boolean isPossible;
    boolean isMarried;
    boolean isWorking;
    boolean isAlive;
    boolean isDead;
    System.out.println("Boolean variables are created");

    //initialization
    isPossible=true;
    isMarried=false;
    isWorking=true;
    isAlive=true;
    isDead=true;

    //access
    System.out.println(isPossible);
    System.out.println(isMarried);
    System.out.println(isWorking);
    System.out.println(isAlive);
    System.out.println(isDead);
  }
}
